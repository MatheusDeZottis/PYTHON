"""
d -  int
f - float
.<número de dífitos>f
x ou x - hexadecimal 
( Caractere )(><^)(quantidade)
> - esquerda
> - direita 
^ -  centro
= força o numero aparece antes do 0 
sinal - + ou - 
ex : 0>-100,.1f
conversion flags -!r !s !a
"""
variavel = 'ABC'
print(f'{ variavel} ')
print(f'{ variavel:<10} ')
print(f'{ variavel:>10} ')
print(f'{ variavel:<10} ')
print(f'{ variavel:^10} ')

